package com.example.demo.model;

public class Inventory {
	
	private Integer premiumCars;
	private Integer suvCars;
	private Integer smallCars;
	
	public Inventory() {

	}
	
	
	public Integer getPremiumCars() {
		return premiumCars;
	}
	public void setPremiumCars(Integer premiumCars) {
		this.premiumCars = premiumCars;
	}
	public Integer getSuvCars() {
		return suvCars;
	}
	public void setSuvCars(Integer suvCars) {
		this.suvCars = suvCars;
	}
	public Integer getSmallCars() {
		return smallCars;
	}
	public void setSmallCars(Integer smallCars) {
		this.smallCars = smallCars;
	}
	
	
	

}


